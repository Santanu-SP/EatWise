export const getRecommendations = (userPrefs = {}, limit = 10) => {
  const { viewedCategories = [], likedIds = [], dietaryTags = [], dietType = 'veg' } = userPrefs;

  let scored = recipes
    .filter(r => {
      if (dietType === 'veg') return r.dietType === 'veg' || r.dietType === 'vegan';
      if (dietType === 'vegan') return r.dietType === 'vegan';
      if (dietType === 'egg') return r.dietType !== 'nonveg';
      return true; // nonveg sees everything
    })
    .map(recipe => {
      let score = recipe.rating * 10;
      if (viewedCategories.includes(recipe.category)) score += 30;
      if (likedIds.length > 0) {
        const likedRecipes = recipes.filter(r => likedIds.includes(r.id));
        const likedTags = likedRecipes.flatMap(r => r.tags);
        score += recipe.tags.filter(t => likedTags.includes(t)).length * 15;
      }
      const dietaryMatches = recipe.tags.filter(t => dietaryTags.includes(t));
      score += dietaryMatches.length * 20;
      if (likedIds.includes(recipe.id)) score -= 50;
      score += Math.random() * 5;
      return { ...recipe, score };
    });

  return scored.sort((a, b) => b.score - a.score).slice(0, limit);
};

export default recipes;
